        // TODO add your handling code here:
        String teamLead = searchProjectTeam.getText();
        
        if(!teamLead.equals("")){
        
           project = bll.searchProjectByTeamLead(teamLead);
           
           if(project!=null){
               
                projectNameExistsField.setText(project.getName() +" Found");
                viewProjectButton.setVisible(true);
           }
           else{
            projectNameExistsField.setVisible(true);
           }
        }


