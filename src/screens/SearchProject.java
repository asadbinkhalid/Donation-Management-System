    private void searchButtonNameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonNameMouseClicked
        // TODO add your handling code here:
        
        String projectName = searchProjectName.getText();
        
        if(!projectName.equals("")){
        
           project  = bll.searchProject(projectName);
           
           if(project!=null){
               
                projectNameExistsField.setText(" Project Found: " + projectName );
               projectNameExistsField.setVisible(true);

                viewProjectButton.setVisible(true);
           }
           else{
                projectNameExistsField.setText("No Projct Found");
               projectNameExistsField.setVisible(true);
               
           }
           
        }

    }//GEN-LAST:event_searchButtonNameMouseClicked

